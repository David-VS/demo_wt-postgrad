package be.ehb;

public class Streekbier {
    private String naam;
    private String brouwerij;

    public Streekbier() {
    }

    public String getNaam() {
        return naam;
    }

    public void setNaam(String naam) {
        this.naam = naam;
    }

    public String getBrouwerij() {
        return brouwerij;
    }

    public void setBrouwerij(String brouwerij) {
        this.brouwerij = brouwerij;
    }
}
